// UNARY (only one data, or one operand)
console.log(! true);

// BINARY (only two data, or two operands)
console.log(3 + 3);


// TERNARY = 3

let a = 10;
if (a > 0) {
    console.log("a is positive");
} else {
    console.log("a is zero or negative");
}

// Ternary operator consists of two symbols--  ? and :
// <cond> ? <value_if_cond_true> : <value_if_cond_false>
console.log( a > 0 ? "a is positive" : "a is zero or negative");


